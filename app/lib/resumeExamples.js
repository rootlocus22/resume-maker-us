// Helper functions
export function getExampleBySlug(slug) {
  return resumeExamples.find(e => e.slug === slug);
}

export function getExamplesByCategory(categorySlug) {
  return resumeExamples.filter(e => e.category === categorySlug);
}

export function getCategoryBySlug(slug) {
  return resumeCategories[slug];
}

export function getAllCategories() {
  return Object.entries(resumeCategories).map(([slug, data]) => ({
    slug,
    ...data,
    count: resumeExamples.filter(e => e.category === slug).length,
  }));
}

export function getAllExampleSlugs() {
  return resumeExamples.map(e => ({
    category: e.category,
    slug: e.slug,
  }));
}

export function getRelatedExamples(slugs) {
  return slugs.map(s => resumeExamples.find(e => e.slug === s)).filter(Boolean);
}
